age = 16

if age < 2:
    print('man is a nursling')
elif age > 2 and age <= 4:
    print('man is a baby')
elif age > 4 and age < 13:
    print('man is a child')
elif age >= 13 and age < 20:
    print('man is a teenager')
elif age >= 20 and age < 65:
    print('man is a adult')
else:
    print('man is an elderly person')
